from supabase import create_client
import os
import pathlib
from dotenv import load_dotenv

load_dotenv(pathlib.Path(__file__).parent.parent / '.env')

sb = create_client(
    os.getenv('VITE_SUPABASE_URL'),
    os.getenv('VITE_SUPABASE_PUBLISHABLE_KEY')
)

print("\n" + "="*70)
print("XGBOOST ML PREDICTIONS IN DATABASE")
print("="*70 + "\n")

result = sb.table('predictions').select('*').order('city').order('year').limit(15).execute()

print(f"Total predictions: {len(result.data)}\n")
print(f"{'City':<30} | Year | Predicted Temp | Confidence")
print("-" * 75)

for p in result.data:
    city = p['city']
    year = p['year']
    temp = p['predicted_temp']
    conf = p['confidence_level']
    print(f"{city:<30} | {year} | {temp:.1f}°C         | {conf:.0%}")

print("\n" + "="*70)
print("✅ These predictions were generated by XGBoost ML model!")
print("="*70 + "\n")
